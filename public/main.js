var trash = document.getElementsByClassName("fa-trash");

var heart = document.getElementsByClassName("fa fa-heart");

var blah = document.getElementsByClassName("fa fa-meh-o");

var fire = document.getElementsByClassName("fa fa-fire");

var save = document.getElementsByClassName("save");


Array.from(save).forEach(function(element) {
      element.addEventListener('click', function(){
        const name = this.parentNode.parentNode.childNodes[1].innerText
        const msg = this.parentNode.parentNode.childNodes[3].innerText
        const topic = this.parentNode.parentNode.childNodes[5].innerText
        const heart = parseFloat(this.parentNode.parentNode.childNodes[7].innerText)
        const src = this.parentNode.parentNode.childNodes[9].getAttribute('src')

        console.log(name)
        console.log(src)

        fetch('favorites', {
          method: 'post',
          headers: {'Content-Type': 'application/json'},
          body: JSON.stringify({
            'name': "",
            'msg': msg,
            'topic':topic,
            'heart':heart,
            'src': src,
            'save': "true"
          })
        })
        .then(response => {
          if (response.ok) return response.json()
        })
        .then(data => {
          console.log(data)
          window.location.reload(true)
        })
      });
});
